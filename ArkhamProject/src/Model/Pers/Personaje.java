package Model.Pers;

public abstract class Personaje {

	private float Fuerza;
	private float Velocidad;
	private float Oro;
	private float Energía;
	private float Sabiduría;
	
	public Personaje(float fuerza, float velocidad, float oro, float energía, float sabiduría) {
		Fuerza = fuerza;
		Velocidad = velocidad;
		Oro = oro;
		Energía = energía;
		Sabiduría = sabiduría;
	}
	
	
	
}
